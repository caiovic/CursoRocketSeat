// transformando a string em letra maiúscula:
let frase = 'Meu nome é caio'
console.log(frase.toUpperCase()) // MEU NOME É CAIO

// transformando em letra minúscula:
let fraseMaiuscula = 'MEU NOME É CAIOOOOOOOO'
console.log(fraseMaiuscula.toLowerCase()) // meu nome é caioooooooo

